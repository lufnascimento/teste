RegisterNUICallback('requestMyCars', function(data,cb)
    local req = vTunnel.requestVehInfos("requestList")
    cb({ list = req })
end)

RegisterNUICallback('SendVehicleSell', function(data,cb)
    vTunnel._requestVehInfos(data.type, data)
end)

exports("setInDesmanche", function(status)
    inDesmanche = status
end)
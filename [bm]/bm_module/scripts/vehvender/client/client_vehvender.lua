RegisterCommand('veh', function(source,args)
    if not inDesmanche then
        SetNuiFocus(true, true)
        SendNUIMessage({ action = "vehvender" })
    else
        TriggerEvent("Notify","negado","Você não pode transferir um veiculo enquanto estiver desmanchando!")
    end
end)


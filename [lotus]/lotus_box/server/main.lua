USER = {}

-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- TUNNELS
-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function CreateTunnel.getMakapoints()
    local source = source
    local user_id = vRP.getUserId(source)
    if not user_id then return end

    if not PlayersTimed[user_id] then
        PlayersTimed[user_id] = os.time()
    end

    return vRP.getMakapoints(user_id) or 0
end

-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- FUNCTIONS
-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function USER:getInventory(user_id)
    local data = vRP.getUData(user_id, 'lotus_box') or { keys = {}, crates = {} }

    return json.decode(data) or { keys = {}, crates = {} }
end

function USER:updateInventory(user_id, data)
    return vRP.setUData(user_id, 'lotus_box', json.encode(data))
end